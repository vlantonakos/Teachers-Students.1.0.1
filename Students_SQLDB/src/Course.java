
public class Course {
	private String Name;
	private String info;
	
	public Course(String Name, String info) {
		this.Name = Name;
		this.info = info;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}
	
	
}
